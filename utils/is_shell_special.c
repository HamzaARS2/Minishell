#include <stdbool.h>

bool	is_shell_special(char c)
{
	if ((c >= 9 && c <= 13) || c == 32 || c == '$' || c == '|' || c == '<'
		|| c == '>' || c == '"' || c == '"' || c == '\'' || c == '\'')
		return (true);
	return (false);
}
